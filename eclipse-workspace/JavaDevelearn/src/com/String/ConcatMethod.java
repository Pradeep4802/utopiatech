package com.String;

public class ConcatMethod {
	public static void main(String[] args) {
		String str = "Rajesh";
		System.out.println(20+30+"Java"+40);
		System.out.println(20+30+"Java"+40+10+30+30);
		System.out.println(20+30+"Java"+(40+10+30+30));
		System.out.println("Pradeep ".concat("Pal"));
		System.out.println("Java".concat("Python").concat("PHP").concat("PERL"));
		System.out.println(str.concat("Jhangle"));
	}
}
